package org.fundamentos;

public class Classes {
    void Encapsulation(){
        /*
            The meaning of Encapsulation, is to make sure that "sensitive" data is hidden from users. To achieve this, you must:

            declare class variables/attributes as private
            provide public get and set methods to access and update the value of a private variable


        private String name; // private = restricted access

        // Getter
        public String getName() {
            return name;
        }

        // Setter
        public void setName(String newName) {
            this.name = newName;
        }

        */



        //  Encapsulation: Ocultar informacion con private y solo dar acceso con metodos
        //  Polymorphism: heredar de otras clases sus metodos y valores
        //  Inheritance(herencia): Mientras la clase no sea tipo final, puede heredar a otras clases sus metodos y valores
        //  Inner Classes: Puedes crear clases dentro de otra clase
        //  Abstraction: Ocultar al informacion original o completa y solo mostrar los valores que queremos vean y como queremos que los vean

    }
}
